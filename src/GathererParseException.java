public class GathererParseException extends Exception {

	private static final long serialVersionUID = 1312707787277591662L;

	public GathererParseException() {
		super();
	}

	public GathererParseException(String message) {
		super(message);
	}

	public GathererParseException(Throwable cause) {
		super(cause);
	}

	public GathererParseException(String message, Throwable cause) {
		super(message, cause);
	}

	public GathererParseException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
